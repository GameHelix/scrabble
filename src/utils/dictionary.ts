// Build the master word set
const WORD_SET = new Set<string>([
  ...TWO_LETTER,
  ...THREE_LETTER,
  ...FOUR_LETTER,
  ...FIVE_LETTER,
  ...SIX_PLUS,
]);

/** Returns true if the given word is valid in Scrabble (case-insensitive) */
export function isValidWord(word: string): boolean {
  return WORD_SET.has(word.toUpperCase());
}

/** Validate a list of words; returns the first invalid word or null */
export function findInvalidWord(words: string[]): string | null {
  for (const w of words) {
    if (!isValidWord(w)) return w;
  }
  return null;
}
